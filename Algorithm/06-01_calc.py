A = []
B = []
C = []

n = 5
m = 5

def calc(n, m):
    A
    for i in range(n):
        B
        i += 1
        print("count {} times B called".format(i))
        for j in range(m):
            C
            j += 1
            print("count {} times C called".format(j))


calc(n, m)

